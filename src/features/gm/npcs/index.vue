<template>
  <div>
    <gm-collection-view
      title="NPCs"
      item-type="Npc"
      :items="npcs"
      @add-new="addNew()"
      @open="openItem($event)"
    />
    <v-dialog v-model="dialog" fullscreen>
      <v-card flat>
        <editor
          v-if="dialog"
          :item="selected"
          @exit="dialog = false"
          @copy="copyItem()"
          @deleteItem="deleteItem()"
          @save="SaveAndClose()"
        >
          <builder slot="upper" :item="selected" />
          <features slot="lower" :item="selected" />
        </editor>
      </v-card>
    </v-dialog>
  </div>
</template>

<script lang="ts">
import Vue from 'vue'
import GmCollectionView from '../_views/GMCollectionView.vue'
import Editor from './editor.vue'
import Builder from './builder.vue'
import Features from './features.vue'
import { getModule } from 'vuex-module-decorators'
import { NpcStore } from '@/store'
import { Npc } from '@/class'

export default Vue.extend({
  name: 'npc-roster',
  components: { GmCollectionView, Editor, Builder, Features },
  data: () => ({
    dialog: false,
    selected: null,
  }),
  computed: {
    npcs() {
      const store = getModule(NpcStore, this.$store)
      return store.Npcs
    },
  },
  methods: {
    openItem(id) {
      this.selected = getModule(NpcStore, this.$store).Npcs.find(x => x.ID === id)
      this.dialog = true
    },
    addNew() {
      // const store = getModule(CompendiumStore, this.$store)

      this.selected = new Npc()
      this.dialog = true
    },
    SaveAndClose() {
      const store = getModule(NpcStore, this.$store)
      // TODO: check for and ask to update instances on save
      store.addNpc(this.selected)
      this.$set(this, 'selected', null)
      this.dialog = false
    },
  },
})
</script>
