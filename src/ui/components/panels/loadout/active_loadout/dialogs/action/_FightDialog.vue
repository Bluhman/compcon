<template>
  <div>
    <action-detail-expander :action="action" />
    <v-divider class="my-3" />
    <v-container style="max-width: 800px">
      <item-selector-row
        v-for="(w, j) in mech.Pilot.Loadout.Weapons"
        :item="w"
        color="action--full"
        @click="fight(w)"
      />
    </v-container>

    <sel-fight-dialog
      ref="f_dialog"
      :pilot="mech.Pilot"
      :item="selected"
      @close="completeFight()"
    />
  </div>
</template>

<script lang="ts">
import ActionDetailExpander from '../../components/_ActionDetailExpander.vue';
import ItemSelectorRow from '../../components/_ItemSelectorRow.vue';
import SelFightDialog from './_SelFightDialog.vue';

import { ActivationType } from '@/classes/enums';

export default {
  name: 'fight-dialog',
  components: { ActionDetailExpander, ItemSelectorRow, SelFightDialog },
  props: {
    mech: {
      type: Object,
      required: true,
    },
    action: {
      type: Object,
      required: true,
    },
  },
  data: () => ({
    dialog: false,
    selected: null,
    selectedMount: null,
  }),
  computed: {
    state() {
      return this.mech.Pilot.State;
    },
  },
  methods: {
    completeFight() {
      this.$emit('use');
      this.state.CommitAction(this.action, ActivationType.Full);
      this.dialog = false;
    },
    fight(item) {
      this.selected = item;
      (this.$refs.f_dialog as any).show();
    },
  },
};
</script>
